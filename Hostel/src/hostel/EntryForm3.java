    private void NextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NextButtonActionPerformed
        // TODO add your handling code here:
        Fname = FatherNameField.getText();
        Mname = MotherNameField.getText();
        Address = AddressTextArea.getText();
        Fnumber = FatherNumberField.getText();
        Mnumber = MotherNumberField.getText();
        
        Error = new InvalidData();
        
        if(Fname.equals(""))
        {
               Error = new InvalidData();
               Error.setVisible(true);
        }
        
        else
        {
            if(Mname.equals(""))
            {
               Error = new InvalidData();
               Error.setVisible(true);
            }
            
            else
            {
                if(Address.equals(""))
                {
                    Error = new InvalidData();
                    Error.setVisible(true);
                }
                
                else
                {
                    if(Mnumber.equals(""))
                    {
                        Error = new InvalidData();
                        Error.setVisible(true);
                    }
                    
                    else
                    {
                        if(Fnumber.equals(""))
                        {
                            Error = new InvalidData();
                            Error.setVisible(true);
                        }
                        
                        else
                        {
                            if(Landline.equals(""))
                            {
                                Error = new InvalidData();
                                Error.setVisible(true);
                            }
                            
                            else
                            { 
                                f2 = new EntryForm4();
                                this.setVisible(false);
                                f2.setVisible(true);
        
                            }
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_NextButtonActionPerformed

    private void LandlineNumberFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_LandlineNumberFieldFocusLost
        // TODO add your handling code here:
        if(LandlineNumberField.getText().equals(""))
            Landline = "";
 
        else
        {
            try
            {
                Landline = LandlineNumberField.getText();
                int p = Integer.parseInt(Landline);
            }

            catch(NumberFormatException a)
            {
                System.out.println("The Format is wrong");
                LandlineNumberField.setText("");
                Error = new InvalidData();
                Error.setVisible(true);
            }
            Landline = LandlineNumberField.getText();
        }    
    }//GEN-LAST:event_LandlineNumberFieldFocusLost

