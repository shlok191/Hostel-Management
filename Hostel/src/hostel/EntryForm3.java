    private void FatherNumberFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_FatherNumberFieldFocusLost
        // TODO add your handling code here:
        if(FatherNumberField.getText().equals(""))
            Fnumber = "";
        
        else
        {
            try
            {
                Fnumber = FatherNumberField.getText();
                long p = Long.parseLong(Fnumber);
            }

            catch(NumberFormatException a)
            {
                System.out.println("The Father Number Format is wrong");
                Error = new InvalidData();
                Error.setVisible(true);
                FatherNumberField.setText("");
            }
            
            Fnumber = FatherNumberField.getText();
        }    
    }//GEN-LAST:event_FatherNumberFieldFocusLost

